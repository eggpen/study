#占位符
#一般常用的占位符如下：
#%d、%.nf、%s：分别表示整数、浮点数、字符串
#语法如下



    #%d
a = int(input() )
print ("%d," %(a))


        #%f浮点数,控制为数 %.2f 保留两位小数
a = 1.23456789  
print("%f"%(a))
print("%10f"%(a)) #和上面一样，输出小数点后6位。此时一共8位(含小数点)，故在左侧补两个空格
print("%010.2f"%(a)) #010表示字符宽10，不够用0填充

print("%7.2f"%(a)) #精确到小数点后2位，共7位(不够左侧补空格)




# %i表示十进制整数，用法
a, b, c = 2015, 11, 1
print ("%04i-%02i-%02i"%(a, b, c)); #其中4和2表示位数,0表示不足规定位数用0填充

